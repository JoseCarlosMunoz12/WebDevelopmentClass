public class Reviewer{
	private int UID;
	private String name;
	private String Bio;
	
	public void setUID(int uid){
		this.UID = uid;
	}
	public void setName(String name){
		this.Name = name;
	}
	public void setBio(String bio){
		this.Bio = bio;
	}
	
	public int getUID(){
		return this.UID;
	}
	public String getName(){
		return this.Name;
	}
	public String getBio(){
		return this.Bio;
	}
}
